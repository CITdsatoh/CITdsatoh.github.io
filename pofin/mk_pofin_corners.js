  function calc(){
  
     let selections=document.querySelectorAll("select.nuts");
     let pofin_maker=new PofinMaker()
     for(var one_person of selections){
       var selected_index=one_person.options.selectedIndex;
       var selected_value=one_person.options[selected_index].value;
       var nuts_no=parseInt(selected_value);
       if(nuts_no !==0){
         nuts_info=all_nuts[nuts_no-1];
         pofin_maker.addNuts(new Nuts(nuts_no,nuts_info[0],nuts_info[1],nuts_info[2]));
       }
     }
     
     let made_pofin=pofin_maker.getPofin();
     dispInfo(pofin_maker,made_pofin);
       
  
  }
  
  function mkOption(){
    
    let all_selections=document.querySelectorAll("select.nuts");
    for(var one_selection of all_selections){
      for(var i=0;i<all_nuts.length;i++){
        var one_option=document.createElement("option");
        one_option.value=""+(i+1);
        one_option.text="No."+(i+1)+":"+all_nuts[i][0];
        one_selection.appendChild(one_option);
      }
   }
   
   let accordition_time_selection=document.querySelector("select[name='accordition']");
   
   
   for(var i=0;i<=10;i++){
     var one_option=document.createElement("option");
     one_option.value=""+i;
     one_option.text=""+i+"回";
     accordition_time_selection.appendChild(one_option);
   }
  
  }
   
  function dispInfo(maker_info,pofin_info){
    
    let pofin_name_input=document.querySelector("input[name='pofin_name']");
    pofin_name_input.value=pofin_info.getPofinName();
     
    let pofin_level_input=document.querySelector("input[name='pofin_level']");
    pofin_level_input.value=pofin_info.getLevel();
     
    let pofin_taste_inputs=document.querySelectorAll("input.tastes");
    for(var i=0;i<pofin_taste_inputs.length;i++){
       pofin_taste_inputs[i].value=pofin_info.tastes[i];
    }
     
     
    let pofin_smooth_input=document.querySelector("input[name='pofin_smooth']");
    pofin_smooth_input.value=pofin_info.smooth;
     
    let pofin_rate_input=document.querySelector("input[name='pofin_rate']");
    pofin_rate_input.value=pofin_info.getRate();
     
     
    let pofin_made_history=document.querySelector("textarea[name='pofin_history']");
    pofin_made_history.value=pofin_made_history.value+maker_info+"\n"+pofin_info+"\n\n";
     
  }
  
  function resetAll(){
    let all_selects=document.querySelectorAll("select");
    for(var one_select of all_selects){
      one_select.selectedIndex=0;
    }
    
    let all_text_inputs=document.querySelectorAll("input[type='text'],textarea");
    for(var one_text_input of all_text_inputs){
      one_text_input.value="";
    }
    
  }
  
 
   